class Solution {
    public int reverse(int x) {
        long ans = 0;
        while (x != 0) {
            ans = ans * 10 + (x % 10);
            x = x / 10;
        }

        return (int)ans == ans ? (int) ans : 0;
    }

    public static void main(String[] args) {
        long l = Long.MAX_VALUE;
        System.out.println(l);
        System.out.println((int)l);
        System.out.println((int) l == l);
    }
}
